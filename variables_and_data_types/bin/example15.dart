void main(List<String> args) {
  const int someInteger = 10;
  print(someInteger);
  const double someDouble = 10.5;
  print(someDouble);
  const String someString = 'Hello World';
  print(someString);
  const bool someBoolean = true;
  print(someBoolean);
  const List<String> someList = ['John', 'Doe', 'Smith'];
  print(someList);
  const Map<String, String> someMap = {'name': 'John Doe', 'age': '30'};
  print(someMap);
  const Set<String> someSet = {'John', 'Doe', 'Smith'};
  print(someSet);
  const dynamic someNull = null;
  print(someNull);
  const Symbol someSymbol = #someNull;
  print(someSymbol);
}
